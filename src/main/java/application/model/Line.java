package application.model;

public class Line {
    private Point start;
    private Point end;
    
    /**
     * @return the start
     */
    public Point getStart() {
	return start;
    }
    /**
     * @param start the start to set
     */
    public void setStart(Point start) {
	this.start = start;
    }
    /**
     * @return the end
     */
    public Point getEnd() {
	return end;
    }
    /**
     * @param end the end to set
     */
    public void setEnd(Point end) {
	this.end = end;
    }
}
